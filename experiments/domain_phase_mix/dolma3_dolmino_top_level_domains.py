def _parse_dolma3_common_crawl_partition(partition_name: str) -> tuple[str, int] | None:
    if not partition_name.startswith("common_crawl/"):
        return None
    _, topic, bucket_str = partition_name.split("/", 2)
    return topic, int(bucket_str)


def _build_dolma3_cc_quality_domain_partitions() -> dict[str, tuple[str, ...]]:
    partitions_by_topic: dict[str, list[tuple[int, str]]] = {}
    for partition_name in DOLMA3_AVAILABLE_TOKEN_COUNTS:
        parsed = _parse_dolma3_common_crawl_partition(partition_name)
        if parsed is None:
            continue
        topic, bucket = parsed
        if topic in REMOVED_DOLMA3_CC_TOPICS:
            continue
        partitions_by_topic.setdefault(topic, []).append((bucket, partition_name))

    quality_domains: dict[str, tuple[str, ...]] = {}
    for topic in DOLMA3_CC_TOPICS:
        if topic in REMOVED_DOLMA3_CC_TOPICS:
            continue
        topic_partitions = sorted(partitions_by_topic[topic])
        low_quality_partitions = tuple(
            partition_name for bucket, partition_name in topic_partitions if bucket <= LOW_QUALITY_MAX_BUCKET
        )
        high_quality_partitions = tuple(
            partition_name for bucket, partition_name in topic_partitions if bucket >= HIGH_QUALITY_MIN_BUCKET
        )
        if not low_quality_partitions or not high_quality_partitions:
            raise ValueError(f"Expected both low/high quality partitions for common crawl topic '{topic}'")
        quality_domains[f"dolma3_cc/{topic}_high"] = high_quality_partitions
        quality_domains[f"dolma3_cc/{topic}_low"] = low_quality_partitions
    return quality_domains


