def _partition_token_count(partition_name: str) -> int:
    try:
        return DOLMA3_AVAILABLE_TOKEN_COUNTS[partition_name]
    except KeyError:
        return DOLMINO_AVAILABLE_TOKEN_COUNTS[partition_name]


