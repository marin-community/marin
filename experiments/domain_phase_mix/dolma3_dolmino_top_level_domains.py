def top_level_domain_partition_counts(domain_name: str) -> dict[str, int]:
    """Return exact partition token counts for one coarse top-level domain."""
    try:
        partitions = TOP_LEVEL_DOMAIN_PARTITIONS[domain_name]
    except KeyError as exc:
        raise KeyError(f"Unknown top-level domain: {domain_name}") from exc

    partition_counts: dict[str, int] = {}
    for partition in partitions:
        if partition in DOLMA3_AVAILABLE_TOKEN_COUNTS:
            partition_counts[partition] = DOLMA3_AVAILABLE_TOKEN_COUNTS[partition]
        else:
            partition_counts[partition] = DOLMINO_AVAILABLE_TOKEN_COUNTS[partition]
    return partition_counts


def all_top_level_domain_names() -> list[str]:
    """Return the top-level domain names in stable loop order."""
    return list(TOP_LEVEL_DOMAIN_PARTITIONS)
