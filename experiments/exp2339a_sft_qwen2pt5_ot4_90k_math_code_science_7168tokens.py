"""
Fine-tunes Qwen/Qwen2.5-7B-Instruct on 90K samples across OpenThoughts4 math+code+science splits where
annotations are generated by Qwen3-32B:
- marin-community/open-thoughts-4-30k-math-qwen3-32b-annotated-32768-tokens
- marin-community/open-thoughts-4-30k-code-qwen3-32b-annotated-32768-tokens
- marin-community/open-thoughts-4-30k-science-qwen3-32b-annotated-32768-tokens
Each split has 29,963 samples.

* This config uses a max sequence length of 7168 tokens. *
"""
import dataclasses
import math

from levanter.data.text import ChatLmDatasetFormat
from levanter.layers.rotary import DefaultRotaryEmbeddingsConfig

from experiments.defaults import default_sft, default_tokenize
from experiments.qwen2pt5_instruct_chat_template import QWEN_2_5_INSTRUCT_CHAT_TEMPLATE
from experiments.qwen3 import qwen2_5_7b_instruct, qwen2_5_7b_instruct_tokenizer
from experiments.posttrain.instruction_datasets import (
    INSTRUCTION_DATASET_NAME_TO_CONFIG,
    get_instruction_dataset,
)
from experiments.simple_sft_config import SimpleSFTConfig, compute_per_device_parallelism
from fray.cluster import ResourceConfig
from marin.execution.executor import ExecutorStep, executor_main
from marin.processing.tokenize import lm_mixture_data_config

def build_dataset_specs() -> tuple[dict[str, str], dict[str, int]]:
    datasets: dict[str, str] = {}
    weights: dict[str, int] = {}
    math_split_name = "marin-community/open-thoughts-4-30k-math-qwen3-32b-annotated-32768-tokens"
    math_split_num_samples = 29963
    code_split_name = "marin-community/open-thoughts-4-30k-code-qwen3-32b-annotated-32768-tokens"
    code_split_num_samples = 29963
    science_split_name = "marin-community/open-thoughts-4-30k-science-qwen3-32b-annotated-32768-tokens"
    science_split_num_samples = 29963
    datasets[math_split_name] = math_split_name
    weights[math_split_name] = math_split_num_samples
    datasets[code_split_name] = code_split_name
    weights[code_split_name] = code_split_num_samples
    datasets[science_split_name] = science_split_name
    weights[science_split_name] = science_split_num_samples
    return datasets, weights


def create_tokenization_step(dataset_identifier: str, short_name: str) -> ExecutorStep:
    dataset_config = INSTRUCTION_DATASET_NAME_TO_CONFIG[dataset_identifier]
    dataset = get_instruction_dataset(dataset_identifier, splits=dataset_config.splits)
    return default_tokenize(
        name=f"{short_name.split('/')[-1]}_qwen2_5_7b_instruct_tokenizer",
        dataset=dataset / "**/*.jsonl.gz",
        tokenizer=qwen2_5_7b_instruct_tokenizer,
        format=ChatLmDatasetFormat(chat_template=QWEN_2_5_INSTRUCT_CHAT_TEMPLATE),
    )


DATASETS, mixture_weights = build_dataset_specs()
tokenized_datasets = {
    short_name: create_tokenization_step(dataset_identifier, short_name)
    for short_name, dataset_identifier in DATASETS.items()
}

assert set(tokenized_datasets.keys()) == set(mixture_weights.keys())

total_examples = sum(mixture_weights.values())
TARGET_EPOCHS = 10
TRAIN_BATCH_SIZE = 128
MICROBATCH_SIZE = 128  # 64 fits on v5p-8 w/ 7K tokens seq len
NUM_TRAIN_STEPS = math.ceil(TARGET_EPOCHS * total_examples / TRAIN_BATCH_SIZE)

RESOURCES = ResourceConfig.with_tpu("v5p-64")

mixture_sft_config = SimpleSFTConfig(
    resources=RESOURCES,
    tokenizer=qwen2_5_7b_instruct_tokenizer,
    model_name_or_path="Qwen/Qwen2.5-7B-Instruct",
    train_batch_size=TRAIN_BATCH_SIZE,
    per_device_parallelism=compute_per_device_parallelism(TRAIN_BATCH_SIZE, MICROBATCH_SIZE, RESOURCES),
    num_train_steps=NUM_TRAIN_STEPS,
    learning_rate=4e-5,
    max_seq_len=7168,
    seed=0,
    steps_per_checkpoint=(total_examples/TRAIN_BATCH_SIZE)//4,  # Every quarter epoch
    lr_schedule="cosine",
    warmup=0.1,
    decay=0.9,
    weight_decay=0.0,
    beta1=0.9,
    beta2=0.999,
    pad_tokenizer_to_match_model=True,  # Model and tokenizer vocab sizes differ
)

mixture_config = lm_mixture_data_config(
    tokenized_datasets,
    mixture_weights,
    permutation_type="feistel",
    shuffle=total_examples,  # IMPORTANT: Era shuffling (shuffle after every epoch). `shuffle=True` leads to same shuffle used in every epoch
    missing_weights_are_validation=True,
    mixture_block_size=12288,  # Doesn't matter for mixtures with 1 dataset
)

qwen2_5_7b_instruct_7168_seq_len = dataclasses.replace(
    qwen2_5_7b_instruct,
    max_seq_len=7168,
    rope=DefaultRotaryEmbeddingsConfig(theta=1_000_000.0),  # 1M theta value (same as original) is appropriate given that the base model was trained with 131K+ context length
    cross_entropy_block_size=32000,  # Process vocab in chunks to reduce memory during loss computation
)

exp2339a_sft_qwen2pt5_ot4_90k = default_sft(
    name="exp2339a_sft_qwen2pt5_ot4_90k_7168tokens",
    tokenized=mixture_config,
    model_config=qwen2_5_7b_instruct_7168_seq_len,
    sft_config=mixture_sft_config,
    tags=["qwen", "openthoughts4", "sft"],
)

exp2339a_checkpoint = exp2339a_sft_qwen2pt5_ot4_90k.cd(f"hf/step-{NUM_TRAIN_STEPS - 1}").nonblocking()

if __name__ == "__main__":
    executor_main(steps=[exp2339a_sft_qwen2pt5_ot4_90k])
