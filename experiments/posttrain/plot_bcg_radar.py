STMT_TO_FAMILY: dict[str, str] = {}
for family, statements in FAMILIES.items():
    for statement in statements:
        STMT_TO_FAMILY[statement] = family

MODELS = [
    ("oracle", "gpt-5.1 (oracle)", "#1f77b4"),
    ("M0", "M0 SFT (marin-8b-instruct)", "#2ca02c"),
    ("M1", "M1 DPO LoRA lr=1e-5", "#d62728"),
]


def load_summaries(path: Path) -> dict:
    if not path.exists():
        raise FileNotFoundError(f"missing comparison summary: {path}")
    return json.loads(path.read_text())


def family_scores(per_point: list[dict], field: str) -> dict[str, float]:
    bucket: dict[str, list[float]] = defaultdict(list)
    for point in per_point:
        if point["feasibility_slice"] != "feasible":
            continue
        statement_a, _, statement_b = point["pair_id"].partition("__")
        families = {STMT_TO_FAMILY.get(statement_a), STMT_TO_FAMILY.get(statement_b)}
        families.discard(None)
        for family in families:
            bucket[family].append(point[field])
    return {family: sum(values) / len(values) if values else float("nan") for family, values in bucket.items()}


def feasible_counts(oracle_points: list[dict]) -> dict[str, int]:
    counts = {family: 0 for family in FAMILIES}
    for point in oracle_points:
        if point["feasibility_slice"] != "feasible":
            continue
        statement_a, _, statement_b = point["pair_id"].partition("__")
        families = {STMT_TO_FAMILY.get(statement_a), STMT_TO_FAMILY.get(statement_b)}
        families.discard(None)
        for family in families:
            counts[family] += 1
    return counts


def radar(ax, axes: list[str], model_rows: list[tuple[str, np.ndarray, str]], title: str) -> None:
    count = len(axes)
    angles = np.linspace(0, 2 * np.pi, count, endpoint=False).tolist()
    angles += angles[:1]
    for label, values, color in model_rows:
        closed = values.tolist() + values[:1].tolist()
        ax.plot(angles, closed, "o-", linewidth=2, label=label, color=color)
        ax.fill(angles, closed, alpha=0.12, color=color)
    ax.set_xticks(angles[:-1])
    ax.set_xticklabels(axes, fontsize=9)
    ax.set_title(title, y=1.08, fontsize=12, fontweight="bold")


def main() -> int:
    output_dir = Path("experiments/posttrain/stage4_output")
    summaries = load_summaries(output_dir / "comparison.json")
    axes = list(FAMILIES.keys())
    counts = feasible_counts(summaries["oracle"]["per_point"])
    axis_labels = [f"{axis}\n(n={counts[axis]})" for axis in axes]

    rows_jsr = []
    rows_bjs = []
    for key, label, color in MODELS:
        per_point = summaries[key]["per_point"]
        jsr = family_scores(per_point, "joint_satisfaction_rate")
        bjs = family_scores(per_point, "balanced_joint_score")
        rows_jsr.append((label, np.array([jsr.get(axis, 0.0) for axis in axes]), color))
        rows_bjs.append((label, np.array([bjs.get(axis, 0.0) for axis in axes]), color))

    fig = plt.figure(figsize=(14, 7))
    ax1 = fig.add_subplot(1, 2, 1, projection="polar")
    ax1.set_ylim(0, 1)
    ax1.set_yticks([0.2, 0.4, 0.6, 0.8])
    ax1.set_yticklabels([".2", ".4", ".6", ".8"], fontsize=8, color="gray")
    radar(ax1, axis_labels, rows_jsr, "Feasible-slice joint satisfaction rate")
    ax1.legend(loc="lower left", bbox_to_anchor=(-0.25, -0.1), fontsize=9)

    ax2 = fig.add_subplot(1, 2, 2, projection="polar")
    ax2.set_ylim(0, 1)
    ax2.set_yticks([0.2, 0.4, 0.6, 0.8])
    ax2.set_yticklabels([".2", ".4", ".6", ".8"], fontsize=8, color="gray")
    radar(ax2, axis_labels, rows_bjs, "Feasible-slice balanced joint score")

    fig.suptitle("Probe profile by semantic family (oracle-feasible slice)", fontsize=14, fontweight="bold")
    fig.tight_layout()
    out = output_dir / "comparison_radar.png"
    fig.savefig(out, dpi=140, bbox_inches="tight")
    plt.close(fig)
    print(f"wrote {out}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
