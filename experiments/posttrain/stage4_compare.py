"""Compare BCG summaries across M0 (SFT), M1 (DPO LoRA lr=1e-5), and the
gpt-5.1 oracle. Produces a paper-ready markdown table, a CSV, and a scatter
plot (M0 BCG vs M1 BCG, one point per tension point).

Reads:
    experiments/posttrain/stage4_output/bcg_M0/bcg_summary.json
    experiments/posttrain/stage4_output/bcg_M1/bcg_summary.json
    experiments/posttrain/stage4_output/bcg_gpt51/bcg_summary.json

Writes:
    experiments/posttrain/stage4_output/comparison.md    (human-readable)
    experiments/posttrain/stage4_output/comparison.csv   (numeric)
    experiments/posttrain/stage4_output/comparison.png   (scatter, optional)
    experiments/posttrain/stage4_output/comparison.json  (machine-readable)

Usage:
    uv run python experiments/posttrain/stage4_compare.py \\
        --output-dir experiments/posttrain/stage4_output
"""

from __future__ import annotations

import argparse
import csv
import json
import logging
import sys
from pathlib import Path

logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
logger = logging.getLogger("stage4_compare")


MODELS = [
    ("oracle", "bcg_gpt51", "gpt-5.1 (oracle)"),
    ("M0", "bcg_M0", "M0 SFT (marin-8b-instruct)"),
    ("M1", "bcg_M1", "M1 DPO LoRA lr=1e-5 seed=0"),
]


def load_summary(output_dir: Path, job_root: str) -> dict | None:
    path = output_dir / job_root / "bcg_summary.json"
    if not path.exists():
        logger.warning("missing %s — skipping", path)
        return None
    return json.loads(path.read_text())


def format_aggregate_row(label: str, agg: dict) -> str:
    if not agg:
        return f"| {label} | — | — | — | — | — |"
    return (
        f"| {label} | {agg.get('n_tension_points', '-')}"
        f" | {agg.get('mean_marginal_A', '-'):.2f}"
        f" | {agg.get('mean_marginal_B', '-'):.2f}"
        f" | {agg.get('mean_joint_satisfaction', '-'):.3f}"
        f" | {agg.get('mean_bcg', '-'):.2f} |"
    )


def write_markdown(summaries: dict[str, dict], output_path: Path) -> str:
    lines = []
    lines.append("# BCG Probe Comparison (3-model × 50 tension points)")
    lines.append("")
    lines.append("Generated by `stage4_compare.py`.")
    lines.append("")
    lines.append("## Aggregate metrics")
    lines.append("")
    lines.append("| model | n_points | mean marginal A | mean marginal B | joint rate | mean BCG |")
    lines.append("|---|---:|---:|---:|---:|---:|")
    for key, _, display in MODELS:
        s = summaries.get(key)
        if s is None:
            lines.append(f"| {display} | — | — | — | — | — |")
        else:
            lines.append(format_aggregate_row(display, s.get("aggregate", {})))

    lines.append("")
    lines.append("## BCG distribution thresholds")
    lines.append("")
    lines.append("| model | BCG > 2 | BCG > 3 | BCG > 4 |")
    lines.append("|---|---:|---:|---:|")
    for key, _, display in MODELS:
        s = summaries.get(key)
        if s is None:
            lines.append(f"| {display} | — | — | — |")
        else:
            a = s.get("aggregate", {})
            lines.append(
                f"| {display} | {a.get('bcg_gt_2_0', '-')}"
                f" | {a.get('bcg_gt_3_0', '-')}"
                f" | {a.get('bcg_gt_4_0', '-')} |"
            )

    # M1 vs M0 per-pair delta
    m0 = summaries.get("M0") or {}
    m1 = summaries.get("M1") or {}
    oracle = summaries.get("oracle") or {}
    m0_pts = {(p["pair_id"], p["tension_point_idx"]): p for p in m0.get("per_point", [])}
    m1_pts = {(p["pair_id"], p["tension_point_idx"]): p for p in m1.get("per_point", [])}
    oracle_pts = {(p["pair_id"], p["tension_point_idx"]): p for p in oracle.get("per_point", [])}
    common = sorted(set(m0_pts) & set(m1_pts))

    if common:
        deltas = [(m1_pts[k]["bcg"] - m0_pts[k]["bcg"], k) for k in common]
        deltas.sort(reverse=True)
        dpo_worse = sum(1 for d, _ in deltas if d > 0.5)
        dpo_better = sum(1 for d, _ in deltas if d < -0.5)
        dpo_neutral = len(common) - dpo_worse - dpo_better

        lines.append("")
        lines.append("## DPO delta (M1 BCG - M0 BCG) per pair")
        lines.append("")
        lines.append(
            f"Out of **{len(common)}** pairs where both models produced scores: "
            f"DPO **worsened** trade-off on **{dpo_worse}** pairs (Δ > 0.5), "
            f"**improved** on **{dpo_better}** (Δ < −0.5), "
            f"**neutral** on **{dpo_neutral}** (|Δ| ≤ 0.5)."
        )
        lines.append("")
        lines.append("### Top 10 pairs where DPO worsened the trade-off")
        lines.append("")
        lines.append("| Δ(M1−M0) | pair | tension | M0 BCG | M1 BCG | oracle BCG |")
        lines.append("|---:|---|---|---:|---:|---:|")
        for d, k in deltas[:10]:
            oracle_bcg = oracle_pts.get(k, {}).get("bcg")
            oracle_str = f"{oracle_bcg:.2f}" if oracle_bcg is not None else "—"
            tname = m1_pts[k].get("tension_name", "")[:60]
            lines.append(
                f"| {d:+.2f} | `{k[0]}` | {tname} "
                f"| {m0_pts[k]['bcg']:.2f} | {m1_pts[k]['bcg']:.2f} | {oracle_str} |"
            )

        if dpo_better > 0:
            lines.append("")
            lines.append("### Top 10 pairs where DPO improved the trade-off")
            lines.append("")
            lines.append("| Δ(M1−M0) | pair | tension | M0 BCG | M1 BCG | oracle BCG |")
            lines.append("|---:|---|---|---:|---:|---:|")
            for d, k in deltas[::-1][:10]:
                if d > -0.5:
                    break
                oracle_bcg = oracle_pts.get(k, {}).get("bcg")
                oracle_str = f"{oracle_bcg:.2f}" if oracle_bcg is not None else "—"
                tname = m1_pts[k].get("tension_name", "")[:60]
                lines.append(
                    f"| {d:+.2f} | `{k[0]}` | {tname} "
                    f"| {m0_pts[k]['bcg']:.2f} | {m1_pts[k]['bcg']:.2f} | {oracle_str} |"
                )

    lines.append("")
    lines.append("## Headline interpretation")
    lines.append("")
    if m1 and m0:
        m1_bcg = m1.get("aggregate", {}).get("mean_bcg", 0)
        m0_bcg = m0.get("aggregate", {}).get("mean_bcg", 0)
        delta = m1_bcg - m0_bcg
        if m1_bcg >= 2.0 and delta >= 0:
            lines.append(
                f"- M1 mean BCG = {m1_bcg:.2f}, M0 mean BCG = {m0_bcg:.2f} (Δ = {delta:+.2f})."
            )
            lines.append("- Paper thesis **supported**: DPO does not fix (and may worsen) trade-offs.")
        elif m1_bcg < 2.0:
            lines.append(
                f"- M1 mean BCG = {m1_bcg:.2f} (below ~2 threshold). Corners may be too easy — consider regenerating from strong_tension set."
            )
        else:
            lines.append(
                f"- M1 mean BCG = {m1_bcg:.2f}, M0 mean BCG = {m0_bcg:.2f} (Δ = {delta:+.2f}). DPO helped modestly; paper would be a weaker claim."
            )
    output = "\n".join(lines) + "\n"
    output_path.write_text(output)
    return output


def write_csv(summaries: dict[str, dict], output_path: Path) -> None:
    m0 = summaries.get("M0") or {}
    m1 = summaries.get("M1") or {}
    oracle = summaries.get("oracle") or {}
    m0_pts = {(p["pair_id"], p["tension_point_idx"]): p for p in m0.get("per_point", [])}
    m1_pts = {(p["pair_id"], p["tension_point_idx"]): p for p in m1.get("per_point", [])}
    oracle_pts = {(p["pair_id"], p["tension_point_idx"]): p for p in oracle.get("per_point", [])}
    keys = sorted(set(m0_pts) | set(m1_pts) | set(oracle_pts))
    with output_path.open("w", newline="") as f:
        w = csv.writer(f)
        w.writerow([
            "pair_id", "tension_point_idx", "tension_name",
            "M0_bcg", "M0_mean_A", "M0_mean_B", "M0_joint",
            "M1_bcg", "M1_mean_A", "M1_mean_B", "M1_joint",
            "oracle_bcg", "oracle_mean_A", "oracle_mean_B", "oracle_joint",
            "M1_minus_M0",
        ])
        for k in keys:
            pid, tp = k
            tname = (m1_pts.get(k) or m0_pts.get(k) or oracle_pts.get(k) or {}).get("tension_name", "")

            def g(pts, field):
                return pts.get(k, {}).get(field)

            m0_bcg = g(m0_pts, "bcg")
            m1_bcg = g(m1_pts, "bcg")
            delta = (m1_bcg - m0_bcg) if (m0_bcg is not None and m1_bcg is not None) else None
            w.writerow([
                pid, tp, tname,
                m0_bcg, g(m0_pts, "mean_A_score"), g(m0_pts, "mean_B_score"), g(m0_pts, "joint_satisfaction_rate"),
                m1_bcg, g(m1_pts, "mean_A_score"), g(m1_pts, "mean_B_score"), g(m1_pts, "joint_satisfaction_rate"),
                g(oracle_pts, "bcg"), g(oracle_pts, "mean_A_score"), g(oracle_pts, "mean_B_score"), g(oracle_pts, "joint_satisfaction_rate"),
                delta,
            ])


def write_scatter(summaries: dict[str, dict], output_path: Path) -> bool:
    """Plot M0 BCG vs M1 BCG per tension point. Returns True if the plot was
    produced, False if matplotlib is unavailable."""
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        logger.warning("matplotlib not available; skipping scatter plot")
        return False

    m0 = summaries.get("M0") or {}
    m1 = summaries.get("M1") or {}
    oracle = summaries.get("oracle") or {}
    m0_pts = {(p["pair_id"], p["tension_point_idx"]): p["bcg"] for p in m0.get("per_point", [])}
    m1_pts = {(p["pair_id"], p["tension_point_idx"]): p["bcg"] for p in m1.get("per_point", [])}
    oracle_pts = {(p["pair_id"], p["tension_point_idx"]): p["bcg"] for p in oracle.get("per_point", [])}
    common = sorted(set(m0_pts) & set(m1_pts))
    if not common:
        logger.warning("no common points for scatter")
        return False

    xs = [m0_pts[k] for k in common]
    ys = [m1_pts[k] for k in common]
    oracles = [oracle_pts.get(k) for k in common]

    fig, ax = plt.subplots(figsize=(7, 7))
    # Color by oracle BCG where available.
    has_oracle = all(o is not None for o in oracles)
    if has_oracle:
        sc = ax.scatter(xs, ys, c=oracles, cmap="viridis", s=45, alpha=0.8, edgecolors="black", linewidth=0.4)
        plt.colorbar(sc, ax=ax, label="oracle BCG (gpt-5.1)")
    else:
        ax.scatter(xs, ys, s=45, alpha=0.7, edgecolors="black", linewidth=0.4)

    # Diagonal.
    lo = min(0, min(xs + ys)) - 0.5
    hi = max(xs + ys) + 0.5
    ax.plot([lo, hi], [lo, hi], color="gray", linestyle="--", linewidth=1, alpha=0.6)
    ax.axhline(0, color="black", linewidth=0.5, alpha=0.3)
    ax.axvline(0, color="black", linewidth=0.5, alpha=0.3)
    ax.set_xlabel("M0 BCG (SFT)")
    ax.set_ylabel("M1 BCG (DPO LoRA lr=1e-5)")
    ax.set_title("BCG per tension point: DPO vs SFT\n(points above diagonal = DPO worsened trade-off)")
    ax.set_xlim(lo, hi)
    ax.set_ylim(lo, hi)
    ax.grid(True, alpha=0.3)
    fig.tight_layout()
    fig.savefig(output_path, dpi=120)
    plt.close(fig)
    logger.info("wrote scatter %s", output_path)
    return True


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--output-dir", required=True, type=Path)
    args = ap.parse_args()

    summaries: dict[str, dict] = {}
    for key, job_root, _ in MODELS:
        s = load_summary(args.output_dir, job_root)
        if s is not None:
            summaries[key] = s

    if not summaries:
        logger.error("no summaries found under %s — did you run compute?", args.output_dir)
        return 2

    # JSON dump (machine-readable)
    (args.output_dir / "comparison.json").write_text(json.dumps(summaries, indent=2))

    md_text = write_markdown(summaries, args.output_dir / "comparison.md")
    write_csv(summaries, args.output_dir / "comparison.csv")
    write_scatter(summaries, args.output_dir / "comparison.png")

    # Print the markdown to stdout so callers can capture it.
    print(md_text)
    return 0


if __name__ == "__main__":
    sys.exit(main())
