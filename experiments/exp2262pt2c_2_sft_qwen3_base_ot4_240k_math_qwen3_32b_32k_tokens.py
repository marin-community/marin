"""
Fine-tunes Qwen/Qwen3-8B-Base on 240K OpenThoughts4 math samples with
annotations generated by Qwen3-32B:
- marin-community/open-thoughts-4-30k-math-qwen3-32b-annotated-32768-tokens-n8-reformatted
This dataset has 239704 samples.

We use a max sequence length of 32768 tokens.
"""
import dataclasses
import math

from levanter.data.text import ChatLmDatasetFormat
from levanter.layers.rotary import DefaultRotaryEmbeddingsConfig

from experiments.defaults import default_sft, default_tokenize
from experiments.chat_templates.qwen3_base_chat_template import QWEN_3_BASE_CHAT_TEMPLATE
from experiments.qwen3 import qwen3_8b_base, qwen3_8b_base_tokenizer
from experiments.posttrain.instruction_datasets import (
    INSTRUCTION_DATASET_NAME_TO_CONFIG,
    get_instruction_dataset,
)
from experiments.simple_sft_config import SimpleSFTConfig, compute_per_device_parallelism
from fray.cluster import ResourceConfig
from marin.execution.executor import ExecutorStep, executor_main
from marin.processing.tokenize import lm_mixture_data_config

DATASET_NAME = "marin-community/open-thoughts-4-30k-math-qwen3-32b-annotated-32768-tokens-n8-reformatted"

def build_dataset_specs() -> tuple[dict[str, str], dict[str, int]]:
    datasets: dict[str, str] = {}
    weights: dict[str, int] = {}
    math_split_name = DATASET_NAME
    math_split_num_samples = 239704
    datasets[math_split_name] = math_split_name
    weights[math_split_name] = math_split_num_samples
    return datasets, weights


def create_tokenization_step(dataset_identifier: str, short_name: str) -> ExecutorStep:
    dataset_config = INSTRUCTION_DATASET_NAME_TO_CONFIG[dataset_identifier]
    dataset = get_instruction_dataset(dataset_identifier, splits=dataset_config.splits)
    return default_tokenize(
        name=f"{short_name.split('/')[-1]}_qwen3_8b_base_tokenizer",
        dataset=dataset / "**/*.jsonl.gz",
        tokenizer=qwen3_8b_base_tokenizer,
        format=ChatLmDatasetFormat(chat_template=QWEN_3_BASE_CHAT_TEMPLATE),
    )


DATASETS, mixture_weights = build_dataset_specs()
tokenized_datasets = {
    short_name: create_tokenization_step(dataset_identifier, short_name)
    for short_name, dataset_identifier in DATASETS.items()
}

assert set(tokenized_datasets.keys()) == set(mixture_weights.keys())

total_examples = sum(mixture_weights.values())
TARGET_EPOCHS = 5
TRAIN_BATCH_SIZE = 256
MODEL_AXIS_SIZE = 1  # No tensor parallelism
MICROBATCH_SIZE = 256  # No gradient accumulation
NUM_TRAIN_STEPS = math.ceil(TARGET_EPOCHS * total_examples / TRAIN_BATCH_SIZE)

RESOURCES = ResourceConfig.with_tpu("v4-512")

mixture_sft_config = SimpleSFTConfig(
    resources=RESOURCES,
    tokenizer=qwen3_8b_base_tokenizer,
    initialize_from_hf="Qwen/Qwen3-8B-Base",
    train_batch_size=TRAIN_BATCH_SIZE,
    model_axis_size=MODEL_AXIS_SIZE,
    per_device_parallelism=compute_per_device_parallelism(TRAIN_BATCH_SIZE, MICROBATCH_SIZE, RESOURCES, MODEL_AXIS_SIZE),
    num_train_steps=NUM_TRAIN_STEPS,
    learning_rate=4e-5,
    max_seq_len=32768,
    seed=0,
    steps_per_checkpoint=(total_examples/TRAIN_BATCH_SIZE)//2,  # Every half epoch
    steps_per_hf_export=(total_examples/TRAIN_BATCH_SIZE)//2,  # Every half epoch
    steps_per_eval=(total_examples/TRAIN_BATCH_SIZE)//2,  # Every half epoch
    lr_schedule="cosine",
    warmup=0.1,
    decay=0.9,
    weight_decay=0.0,
    beta1=0.9,
    beta2=0.999,
    pad_tokenizer_to_match_model=True,  # Model and tokenizer vocab sizes differ
)

NUM_VALIDATION_SEQUENCES = 8000  # exp2262pt2a uses 1000, so we use 8000 here to use the same set of prompts for the val set

mixture_config = lm_mixture_data_config(
    tokenized_datasets,
    mixture_weights,
    shuffle=total_examples,  # IMPORTANT: Era shuffling (shuffle after every epoch). `shuffle=True` leads to same shuffle used in every epoch
    missing_weights_are_validation=True,
    mixture_block_size=12288,  # Doesn't matter for mixtures with 1 dataset
    num_validation_sequences={DATASET_NAME: NUM_VALIDATION_SEQUENCES},  # For validation set
    shuffle_before_trainval_split=False,  # Do not shuffle before splitting data into train/val
)

qwen3_8b_32768_seq_len = dataclasses.replace(
    qwen3_8b_base,
    max_seq_len=32768,
    rope=DefaultRotaryEmbeddingsConfig(theta=1_000_000.0),  # 1M theta value (same as original) is appropriate given that the base model was trained with 131K+ context length
)

exp2262pt2c_2_sft_qwen3_base_ot4_240k_math = default_sft(
    name="exp2262pt2c_2_sft_qwen3_base_ot4_240k_math_qwen3_32b_32768tokens_bsz256",
    tokenized=mixture_config,
    model_config=qwen3_8b_32768_seq_len,
    sft_config=mixture_sft_config,
    tags=["qwen", "openthoughts4", "sft"],
)

exp2262pt2c_2_checkpoint = exp2262pt2c_2_sft_qwen3_base_ot4_240k_math.cd(f"hf/step-{NUM_TRAIN_STEPS - 1}").nonblocking()

if __name__ == "__main__":
    executor_main(steps=[exp2262pt2c_2_sft_qwen3_base_ot4_240k_math])
