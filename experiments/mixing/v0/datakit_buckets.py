"""Bucket token table for the datakit us-central2 store (store_8ac06c74).

Pure data: the (cluster, quality, total_tokens) split of the 200 tokenized
caches into the 167 buckets large enough to mix on their own at the maximum
mixture block size (65535) and the 33 below that floor (folded into ``tail``).
Dependency-free so both the mixture experiment (``datakit_moe_mix``) and the
standalone swarm dashboard can read it without importing Levanter/Marin.
"""


def _bucket_name(cluster: int, quality: int) -> str:
    return f"c{cluster:02d}q{quality}"


