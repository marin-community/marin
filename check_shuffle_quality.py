#!/usr/bin/env python3
"""
Check shuffle quality of generated parquet files.

This script analyzes the shuffle quality of parquet files generated by
convert_llava_onevision_to_levanter.py using statistical tests.

Usage:
    python check_shuffle_quality.py --data-dir /run/user/2018/llava_10m/
"""

import argparse
import json
import pandas as pd
import numpy as np
from pathlib import Path
from typing import List, Dict, Any
from scipy import stats


def load_shard_stats(parquet_path: str) -> Dict[str, Any]:
    """Load a shard and compute statistics."""
    df = pd.read_parquet(parquet_path)

    # Extract caption lengths, image sizes, and sources
    caption_lengths = []
    image_sizes = []
    sources = []

    for idx, row in df.iterrows():
        messages = row['messages']
        # Extract assistant response (caption)
        for msg in messages:
            if msg['role'] == 'assistant':
                content = msg['content']
                for item in content:
                    if item.get('type') == 'text':
                        text = item.get('text', '')
                        if text:
                            caption_lengths.append(len(text))

        # Extract image size
        images = row.get('images', [])
        if images and len(images) > 0:
            img_data = images[0]
            if isinstance(img_data, dict) and 'bytes' in img_data:
                img_bytes = img_data['bytes']
                if img_bytes:
                    image_sizes.append(len(img_bytes))

        # Extract source (if available)
        source = row.get('source', 'unknown')
        if source is None:
            source = 'unknown'
        sources.append(source)

    return {
        'caption_lengths': caption_lengths,
        'image_sizes': image_sizes,
        'sources': sources,
        'num_rows': len(df)
    }


def compute_distribution_stats(values: List[float]) -> Dict[str, float]:
    """Compute distribution statistics."""
    if not values:
        return {
            'mean': 0.0, 'std': 0.0, 'median': 0.0,
            'p25': 0.0, 'p75': 0.0, 'min': 0.0, 'max': 0.0
        }
    arr = np.array(values)
    return {
        'mean': float(np.mean(arr)),
        'std': float(np.std(arr)),
        'median': float(np.median(arr)),
        'p25': float(np.percentile(arr, 25)),
        'p75': float(np.percentile(arr, 75)),
        'min': float(np.min(arr)),
        'max': float(np.max(arr))
    }


def runs_test(values: List[float], threshold: float = None) -> Dict[str, Any]:
    """
    Wald-Wolfowitz runs test for randomness.
    Tests if values above/below median alternate randomly.

    A "run" is a sequence of consecutive values all above or all below the median.
    If data is random, runs should alternate frequently.
    Too few runs = clustering (bad shuffle)
    """
    if len(values) < 20:
        return {'runs': 0, 'p_value': 1.0, 'conclusion': 'insufficient_data'}

    arr = np.array(values)
    if threshold is None:
        threshold = np.median(arr)

    # Convert to binary: above median = 1, below = 0
    binary = (arr > threshold).astype(int)

    # Count runs
    runs = 1
    for i in range(1, len(binary)):
        if binary[i] != binary[i-1]:
            runs += 1

    # Expected runs under randomness
    n1 = int(np.sum(binary))
    n2 = len(binary) - n1

    if n1 == 0 or n2 == 0:
        return {'runs': runs, 'p_value': 1.0, 'conclusion': 'insufficient_data'}

    expected_runs = (2 * n1 * n2) / (n1 + n2) + 1
    variance = (2 * n1 * n2 * (2 * n1 * n2 - n1 - n2)) / ((n1 + n2)**2 * (n1 + n2 - 1))

    if variance <= 0:
        return {'runs': runs, 'p_value': 1.0, 'conclusion': 'insufficient_variance'}

    z = (runs - expected_runs) / np.sqrt(variance)
    p_value = 2 * (1 - stats.norm.cdf(abs(z)))

    return {
        'runs': runs,
        'expected_runs': float(expected_runs),
        'z_score': float(z),
        'p_value': float(p_value),
        'conclusion': 'random' if p_value > 0.05 else 'non_random'
    }


def anova_test(shard_values: List[List[float]]) -> Dict[str, Any]:
    """
    One-way ANOVA to test if shard means differ significantly.
    Good shuffle: p-value should be high (means are similar across shards).
    """
    # Filter out empty lists
    non_empty = [v for v in shard_values if len(v) > 0]
    if len(non_empty) < 2:
        return {'f_statistic': 0.0, 'p_value': 1.0, 'conclusion': 'insufficient_data'}

    try:
        f_stat, p_value = stats.f_oneway(*non_empty)
        if np.isnan(f_stat) or np.isnan(p_value):
            return {'f_statistic': 0.0, 'p_value': 1.0, 'conclusion': 'insufficient_data'}
    except Exception:
        return {'f_statistic': 0.0, 'p_value': 1.0, 'conclusion': 'error'}

    return {
        'f_statistic': float(f_stat),
        'p_value': float(p_value),
        'conclusion': 'similar_distributions' if p_value > 0.05 else 'different_distributions'
    }


def check_shuffle_quality(data_dir: str) -> Dict[str, Any]:
    """Main function to check shuffle quality."""
    data_path = Path(data_dir)
    parquet_files = sorted(data_path.glob('train-*.parquet'))

    if not parquet_files:
        print(f"No parquet files found in {data_dir}")
        return {}

    print(f"Found {len(parquet_files)} parquet shards")

    # Load all shard stats
    all_shard_stats = []
    all_caption_lengths = []
    all_image_sizes = []
    all_sources = []

    for pf in parquet_files:
        print(f"Processing {pf.name}...")
        shard_stats = load_shard_stats(str(pf))
        all_shard_stats.append(shard_stats)
        all_caption_lengths.extend(shard_stats['caption_lengths'])
        all_image_sizes.extend(shard_stats['image_sizes'])
        all_sources.extend(shard_stats['sources'])

    results = {
        'num_shards': len(parquet_files),
        'total_rows': sum(s['num_rows'] for s in all_shard_stats)
    }

    # ========== SOURCE DISTRIBUTION ANALYSIS ==========
    from collections import Counter

    # Overall source distribution
    overall_source_counts = Counter(all_sources)
    total_with_source = sum(overall_source_counts.values())
    results['overall_source_distribution'] = {
        source: {
            'count': count,
            'percentage': round(count / total_with_source * 100, 2) if total_with_source > 0 else 0
        }
        for source, count in sorted(overall_source_counts.items(), key=lambda x: -x[1])
    }

    # Check if we have meaningful source data
    has_source_data = len(overall_source_counts) > 1 or 'unknown' not in overall_source_counts

    # Per-shard source distribution
    results['per_shard_source_distribution'] = []
    for i, ss in enumerate(all_shard_stats):
        shard_source_counts = Counter(ss['sources'])
        shard_total = sum(shard_source_counts.values())
        shard_dist = {
            source: {
                'count': count,
                'percentage': round(count / shard_total * 100, 2) if shard_total > 0 else 0
            }
            for source, count in sorted(shard_source_counts.items(), key=lambda x: -x[1])
        }
        results['per_shard_source_distribution'].append({
            'shard_idx': i,
            'sources': shard_dist
        })

    # Chi-squared test for uniformity of source distribution across shards
    if has_source_data and len(parquet_files) > 1:
        # Build contingency table: rows = sources, columns = shards
        all_source_names = sorted(overall_source_counts.keys())
        contingency_table = []
        for source in all_source_names:
            row = [Counter(ss['sources']).get(source, 0) for ss in all_shard_stats]
            contingency_table.append(row)

        contingency_array = np.array(contingency_table)

        # Only run chi-squared if we have enough data
        if contingency_array.sum() > 0 and contingency_array.shape[0] > 1:
            try:
                chi2, p_value, dof, expected = stats.chi2_contingency(contingency_array)
                results['source_uniformity_test'] = {
                    'chi2_statistic': float(chi2),
                    'p_value': float(p_value),
                    'degrees_of_freedom': int(dof),
                    'conclusion': 'uniform_distribution' if p_value > 0.05 else 'non_uniform_distribution',
                    'interpretation': 'Sources are evenly distributed across shards' if p_value > 0.05 else 'Sources are NOT evenly distributed across shards'
                }
            except Exception as e:
                results['source_uniformity_test'] = {
                    'error': str(e),
                    'conclusion': 'test_failed'
                }
        else:
            results['source_uniformity_test'] = {
                'conclusion': 'insufficient_data',
                'interpretation': 'Not enough source variety for chi-squared test'
            }
    else:
        results['source_uniformity_test'] = {
            'conclusion': 'no_source_data' if not has_source_data else 'single_shard',
            'interpretation': 'No source data available (all unknown) or single shard' if not has_source_data else 'Only one shard, cannot test distribution'
        }

    results['has_source_data'] = has_source_data

    # 1. Overall distribution stats
    results['overall_caption_stats'] = compute_distribution_stats(all_caption_lengths)
    results['overall_image_stats'] = compute_distribution_stats(all_image_sizes)

    # 2. Per-shard distribution stats
    shard_caption_means = []
    shard_caption_stds = []
    shard_image_means = []

    for i, ss in enumerate(all_shard_stats):
        if ss['caption_lengths']:
            shard_stats = compute_distribution_stats(ss['caption_lengths'])
            shard_caption_means.append(shard_stats['mean'])
            shard_caption_stds.append(shard_stats['std'])
        if ss['image_sizes']:
            shard_image_means.append(np.mean(ss['image_sizes']))

    # Shard-to-shard consistency
    if shard_caption_means and np.mean(shard_caption_means) > 0:
        results['shard_caption_mean_variation'] = {
            'mean_of_means': float(np.mean(shard_caption_means)),
            'std_of_means': float(np.std(shard_caption_means)),
            'cv': float(np.std(shard_caption_means) / np.mean(shard_caption_means))
        }
    else:
        results['shard_caption_mean_variation'] = {
            'mean_of_means': 0.0, 'std_of_means': 0.0, 'cv': 0.0
        }

    if shard_image_means and np.mean(shard_image_means) > 0:
        results['shard_image_mean_variation'] = {
            'mean_of_means': float(np.mean(shard_image_means)),
            'std_of_means': float(np.std(shard_image_means)),
            'cv': float(np.std(shard_image_means) / np.mean(shard_image_means))
        }
    else:
        results['shard_image_mean_variation'] = {
            'mean_of_means': 0.0, 'std_of_means': 0.0, 'cv': 0.0
        }

    # 3. Runs test on caption lengths (check for clustering)
    results['runs_test_caption_length'] = runs_test(all_caption_lengths)

    # 4. ANOVA test
    shard_caption_lists = [s['caption_lengths'] for s in all_shard_stats]
    results['anova_caption_length'] = anova_test(shard_caption_lists)

    # 5. Overall quality score
    quality_indicators = []

    # Low CV of shard means = good (distributions are consistent across shards)
    cv = results['shard_caption_mean_variation']['cv']
    quality_indicators.append(1.0 if cv < 0.1 else 0.5 if cv < 0.2 else 0.0)

    # High ANOVA p-value = good (similar distributions across shards)
    anova_p = results['anova_caption_length']['p_value']
    quality_indicators.append(1.0 if anova_p > 0.1 else 0.5 if anova_p > 0.01 else 0.0)

    # Runs test shows randomness = good
    runs_p = results['runs_test_caption_length']['p_value']
    quality_indicators.append(1.0 if runs_p > 0.05 else 0.5 if runs_p > 0.01 else 0.0)

    results['quality_score'] = float(np.mean(quality_indicators))
    results['quality_grade'] = (
        'Excellent' if results['quality_score'] >= 0.9 else
        'Good' if results['quality_score'] >= 0.7 else
        'Fair' if results['quality_score'] >= 0.5 else
        'Poor'
    )

    # Per-shard summary
    results['per_shard_summary'] = []
    for i, ss in enumerate(all_shard_stats):
        shard_summary = {
            'shard_idx': i,
            'num_rows': ss['num_rows'],
        }
        if ss['caption_lengths']:
            shard_summary['caption_mean'] = float(np.mean(ss['caption_lengths']))
            shard_summary['caption_std'] = float(np.std(ss['caption_lengths']))
        if ss['image_sizes']:
            shard_summary['image_mean_kb'] = float(np.mean(ss['image_sizes']) / 1024)
        results['per_shard_summary'].append(shard_summary)

    return results


def print_report(results: Dict[str, Any]):
    """Print human-readable report."""
    print("\n" + "=" * 60)
    print("SHUFFLE QUALITY REPORT")
    print("=" * 60)

    print(f"\nDataset Overview:")
    print(f"  Shards: {results['num_shards']}")
    print(f"  Total rows: {results['total_rows']:,}")

    print(f"\nCaption Length Statistics:")
    cs = results['overall_caption_stats']
    print(f"  Mean: {cs['mean']:.1f} chars")
    print(f"  Std: {cs['std']:.1f} chars")
    print(f"  Range: [{cs['min']:.0f}, {cs['max']:.0f}]")

    print(f"\nImage Size Statistics:")
    ims = results['overall_image_stats']
    print(f"  Mean: {ims['mean']/1024:.1f} KB")
    print(f"  Std: {ims['std']/1024:.1f} KB")
    print(f"  Range: [{ims['min']/1024:.0f}, {ims['max']/1024:.0f}] KB")

    print(f"\nShard-to-Shard Consistency (Caption Length):")
    sv = results['shard_caption_mean_variation']
    print(f"  Mean of shard means: {sv['mean_of_means']:.1f}")
    print(f"  Std of shard means: {sv['std_of_means']:.1f}")
    print(f"  Coefficient of variation: {sv['cv']:.4f}")
    cv_assessment = 'Good' if sv['cv'] < 0.1 else 'Fair' if sv['cv'] < 0.2 else 'Poor'
    print(f"  Assessment: {cv_assessment} (lower CV is better)")

    print(f"\nShard-to-Shard Consistency (Image Size):")
    iv = results['shard_image_mean_variation']
    print(f"  Mean of shard means: {iv['mean_of_means']/1024:.1f} KB")
    print(f"  Std of shard means: {iv['std_of_means']/1024:.1f} KB")
    print(f"  Coefficient of variation: {iv['cv']:.4f}")

    print(f"\nANOVA Test (are shard distributions similar?):")
    anova = results['anova_caption_length']
    print(f"  F-statistic: {anova['f_statistic']:.2f}")
    print(f"  p-value: {anova['p_value']:.4f}")
    print(f"  Conclusion: {anova['conclusion']}")
    print(f"  (High p-value = good, means shards have similar distributions)")

    print(f"\nRuns Test (is data order random?):")
    runs = results['runs_test_caption_length']
    print(f"  Observed runs: {runs['runs']}")
    if 'expected_runs' in runs:
        print(f"  Expected runs: {runs['expected_runs']:.1f}")
    print(f"  p-value: {runs['p_value']:.4f}")
    print(f"  Conclusion: {runs['conclusion']}")
    print(f"  (High p-value = good, means data is randomly ordered)")

    print(f"\nPer-Shard Statistics:")
    print(f"  {'Shard':<8} {'Rows':<8} {'Caption Mean':<14} {'Caption Std':<12} {'Image KB':<10}")
    print(f"  {'-'*8} {'-'*8} {'-'*14} {'-'*12} {'-'*10}")
    for ss in results['per_shard_summary']:
        caption_mean = ss.get('caption_mean', 0)
        caption_std = ss.get('caption_std', 0)
        image_kb = ss.get('image_mean_kb', 0)
        print(f"  {ss['shard_idx']:<8} {ss['num_rows']:<8} {caption_mean:<14.1f} {caption_std:<12.1f} {image_kb:<10.1f}")

    # ========== SOURCE DISTRIBUTION ==========
    print(f"\n" + "=" * 60)
    print("SOURCE DISTRIBUTION ANALYSIS")
    print("=" * 60)

    if results.get('has_source_data', False):
        print(f"\nOverall Source Distribution:")
        for source, info in results['overall_source_distribution'].items():
            print(f"  {source}: {info['count']:,} ({info['percentage']:.1f}%)")

        print(f"\nPer-Shard Source Distribution:")
        # Get all source names
        all_sources = list(results['overall_source_distribution'].keys())

        # Print header
        header = "  Shard"
        for src in all_sources[:6]:  # Limit to first 6 sources for display
            header += f"  {src[:10]:>10}"
        print(header)
        print("  " + "-" * (6 + len(all_sources[:6]) * 12))

        # Print each shard's distribution
        for shard_dist in results['per_shard_source_distribution']:
            row = f"  {shard_dist['shard_idx']:<5}"
            for src in all_sources[:6]:
                info = shard_dist['sources'].get(src, {'count': 0, 'percentage': 0})
                row += f"  {info['count']:>5}({info['percentage']:>4.1f}%)"
            print(row)

        # Source uniformity test
        print(f"\nSource Uniformity Test (Chi-squared):")
        sut = results.get('source_uniformity_test', {})
        if 'chi2_statistic' in sut:
            print(f"  Chi-squared statistic: {sut['chi2_statistic']:.2f}")
            print(f"  p-value: {sut['p_value']:.4f}")
            print(f"  Degrees of freedom: {sut['degrees_of_freedom']}")
            print(f"  Conclusion: {sut['conclusion']}")
            print(f"  Interpretation: {sut['interpretation']}")
        else:
            print(f"  Conclusion: {sut.get('conclusion', 'N/A')}")
            print(f"  Interpretation: {sut.get('interpretation', 'N/A')}")
    else:
        print(f"\n  No source data available (all sources are 'unknown').")
        print(f"  To enable source analysis, regenerate the data with the latest")
        print(f"  convert_llava_onevision_to_levanter.py which includes source field.")

    print(f"\n" + "=" * 60)
    print(f"OVERALL QUALITY SCORE: {results['quality_score']:.2f}")
    print(f"GRADE: {results['quality_grade']}")
    print("=" * 60)

    # Interpretation guide
    print(f"\nInterpretation Guide:")
    print(f"  - CV < 0.1: Excellent consistency across shards")
    print(f"  - ANOVA p > 0.05: Shard distributions are statistically similar")
    print(f"  - Runs test p > 0.05: Data order appears random (good shuffle)")
    print(f"  - Overall score >= 0.9: Excellent, >= 0.7: Good, >= 0.5: Fair, < 0.5: Poor")


def main():
    parser = argparse.ArgumentParser(
        description='Check shuffle quality of generated parquet files',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
    python check_shuffle_quality.py --data-dir /run/user/2018/llava_10m/
    python check_shuffle_quality.py --data-dir /tmp/output/ --output-report report.json
        """
    )
    parser.add_argument(
        '--data-dir',
        required=True,
        help='Directory containing parquet files (train-*.parquet)'
    )
    parser.add_argument(
        '--output-report',
        default='shuffle_quality_report.json',
        help='Output JSON report path (default: shuffle_quality_report.json)'
    )

    args = parser.parse_args()

    results = check_shuffle_quality(args.data_dir)

    if not results:
        print("No results to report.")
        return

    print_report(results)

    # Save JSON report
    with open(args.output_report, 'w') as f:
        json.dump(results, f, indent=2)
    print(f"\nDetailed report saved to: {args.output_report}")


if __name__ == '__main__':
    main()
