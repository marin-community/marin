def _convert_image_to_base64(img) -> str:
    """Convert a PIL Image or other image format to base64 string."""
    from PIL import Image

    if isinstance(img, str):
        if img.startswith("base64:"):
            return img
        # It's a path - read and encode
        with open(img, "rb") as f:
            return "base64:" + base64.b64encode(f.read()).decode("utf-8")
    elif isinstance(img, bytes):
        return "base64:" + base64.b64encode(img).decode("utf-8")
    elif isinstance(img, Image.Image):
        # Convert PIL Image to PNG bytes then base64
        buffer = io.BytesIO()
        img.save(buffer, format="PNG")
        return "base64:" + base64.b64encode(buffer.getvalue()).decode("utf-8")
    elif isinstance(img, dict) and "bytes" in img:
        # HuggingFace format: {"bytes": b"...", "path": ...}
        return "base64:" + base64.b64encode(img["bytes"]).decode("utf-8")
    else:
        return ""


def save_packed_parquet(packed_samples: list, output_path: str) -> None:
    """Save pre-packed samples to parquet file.

    This saves data in the format expected by PreprocessedPackedVLMDataset:
    - input_ids, attention_mask, loss_mask (packed sequences)
    - segment_ids, position_ids (packing info)
    - images (base64 encoded strings)
    - image_segment_ids, num_segments, num_images
    """
    schema = pa.schema([
        ("input_ids", pa.list_(pa.int32())),
        ("attention_mask", pa.list_(pa.int32())),
        ("loss_mask", pa.list_(pa.float32())),
        ("segment_ids", pa.list_(pa.int32())),
        ("position_ids", pa.list_(pa.int32())),
        ("images", pa.list_(pa.string())),  # base64 encoded
        ("image_segment_ids", pa.list_(pa.int32())),
        ("num_segments", pa.int32()),
        ("num_images", pa.int32()),
    ])

    columns = {
        "input_ids": [s["input_ids"] for s in packed_samples],
        "attention_mask": [s["attention_mask"] for s in packed_samples],
        "loss_mask": [s["loss_mask"] for s in packed_samples],
        "segment_ids": [s["segment_ids"] for s in packed_samples],
        "position_ids": [s["position_ids"] for s in packed_samples],
        "images": [s["images"] for s in packed_samples],
        "image_segment_ids": [s["image_segment_ids"] for s in packed_samples],
        "num_segments": [s["num_segments"] for s in packed_samples],
        "num_images": [s["num_images"] for s in packed_samples],
    }

    table = pa.table(columns, schema=schema)
    pq.write_table(table, output_path)


def preprocess_and_pack_samples(
    samples: list,
    processor,
    tokenizer,
    config: PreprocessConfig,
    max_patches: int,
    max_segments: int,
) -> list:
    """
    Preprocess samples and pack them using the same logic as preprocess_vlm_data.py --enable-packing.

    This simulates offline preprocessing with packing.

    Args:
        samples: List of raw samples (with messages and images)
        processor: HuggingFace processor
        tokenizer: HuggingFace tokenizer
        config: Preprocessing configuration
        max_patches: Maximum patches per pack
        max_segments: Maximum segments per pack

    Returns:
        List of packed samples ready for PreprocessedPackedVLMDataset
    """
    # 1. Preprocess each sample
    preprocessed = []
    for sample in samples:
        p = preprocess_sample(sample, processor, tokenizer, config)
        preprocessed.append(p)

    # 2. Compute token lengths and patch counts for packing
    token_lengths = []
    patch_counts = []
    for p in preprocessed:
        token_count = int(np.sum(p["attention_mask"]))
        token_lengths.append(token_count)
        patch_counts.append(p["num_images"])  # With disable_anyres, 1 image = 1 patch

    # 3. Run greedy packing
    packs = greedy_pack(
        token_lengths,
        patch_counts,
        max_tokens=config.max_length,
        max_patches=max_patches,
        max_segments=max_segments,
    )

    # 4. Assemble packed samples
    packed_samples = []
    pad_token_id = tokenizer.pad_token_id or 0

    for pack_indices in packs:
        pack_samples = [preprocessed[i] for i in pack_indices]

        # Serialize images to base64 strings
        for s in pack_samples:
            s["images"] = [_convert_image_to_base64(img) for img in s.get("images", [])]

        packed = assemble_pack(pack_samples, config.max_length, max_patches, pad_token_id)
        packed_samples.append(packed)

    return packed_samples


@pytest.mark.entry
@skip_if_no_torch
def test_preprocessed_packed_data_matches_direct_streaming():
    """
    Verify: preprocess_vlm_data.py (with packing) + PreprocessedPackedVLMDataset
    produces same packed data as BatchImageProcessor + runtime packing.

    This tests that offline packing (done during preprocessing) produces the same
    results as runtime packing (done by VLMPrepackedDataset).

    Flow:
    1. Load raw data and save to parquet
    2. Path A: raw parquet → create_streaming_packed_dataset (BatchImageProcessor + runtime packing) → packed sample
    3. Path B: raw data → preprocess_and_pack_samples (offline packing) → PreprocessedPackedVLMDataset → packed sample
    4. Compare all packed fields
    """
    from transformers import AutoTokenizer, AutoProcessor
    from levanter.data.vlm_packing import PreprocessedPackedVLMDataset

    num_samples = 2  # Pack 2 samples together

    # Load tokenizer and processor
    qwen3_tokenizer = AutoTokenizer.from_pretrained(QWEN3_TOKENIZER, trust_remote_code=True)
    processor = AutoProcessor.from_pretrained(MODEL_NAME, trust_remote_code=True)

    # Preprocessing config
    config = PreprocessConfig(
        max_length=MAX_LENGTH,
        features_per_patch=FEATURES_PER_PATCH,
    )

    with tempfile.TemporaryDirectory() as tmpdir:
        # 1. Load raw data and save to parquet
        hf_dataset = get_real_data(num_samples=num_samples)
        raw_parquet = f"{tmpdir}/raw.parquet"
        hf_dataset.to_parquet(raw_parquet)

        # 2. Path A: create_streaming_packed_dataset (BatchImageProcessor + runtime packing)
        # In disable_anyres mode, each sample has exactly 1 patch
        max_patches = num_samples  # 2 samples with 1 patch each = 2 patches

        packed_dataset_A = create_streaming_packed_dataset(
            parquet_path=raw_parquet,
            tmpdir=f"{tmpdir}/path_a",
            processor=processor,
            tokenizer=qwen3_tokenizer,
            max_length=MAX_LENGTH,
            max_patches=max_patches,
            max_segments=num_samples,
            features_per_patch=FEATURES_PER_PATCH,
            disable_anyres=True,
            grid_pinpoints=SINGLE_PATCH_GRID_PINPOINTS,
            vision_feature_height=27,
        )
        packed_A_batch = asyncio.get_event_loop().run_until_complete(
            packed_dataset_A.get_batch([0])
        )
        packed_A = packed_A_batch[0]

        # 3. Path B: preprocess + offline packing → PreprocessedPackedVLMDataset
        # 3a. Preprocess and pack samples (simulates preprocess_vlm_data.py --enable-packing)
        raw_samples = [hf_dataset[i] for i in range(num_samples)]
        packed_samples = preprocess_and_pack_samples(
            samples=raw_samples,
            processor=processor,
            tokenizer=qwen3_tokenizer,
            config=config,
            max_patches=max_patches,
            max_segments=num_samples,
        )

        # 3b. Save packed samples to parquet
        packed_parquet = f"{tmpdir}/packed.parquet"
        save_packed_parquet(packed_samples, packed_parquet)

        # 3c. Load with PreprocessedPackedVLMDataset
        packed_dataset_B = PreprocessedPackedVLMDataset(
            parquet_paths=[packed_parquet],
            image_processor=processor.image_processor,
            max_num_patches=max_patches,
            patch_size=384,
        )
        packed_B_batch = asyncio.get_event_loop().run_until_complete(
            packed_dataset_B.get_batch([0])
        )
        packed_B = packed_B_batch[0]

        # 4. Verify packing worked
        assert packed_A['num_segments'] == num_samples, f"Path A: Expected {num_samples} segments, got {packed_A['num_segments']}"
        assert packed_B['num_segments'] == num_samples, f"Path B: Expected {num_samples} segments, got {packed_B['num_segments']}"

        print(f"\n=== Packed Data Comparison ===")
        print(f"  Path A (streaming+runtime packing): {packed_A['num_segments']} segments, seq_len={len(packed_A['input_ids'])}")
        print(f"  Path B (offline packing+PreprocessedPackedVLMDataset): {packed_B['num_segments']} segments, seq_len={len(packed_B['input_ids'])}")

        # 5. Compare all packed fields
        all_passed = True

        # input_ids comparison
        try:
            np.testing.assert_array_equal(
                packed_A["input_ids"],
                packed_B["input_ids"],
            )
            print(f"  input_ids: PASS (len={len(packed_A['input_ids'])})")
        except AssertionError as e:
            print(f"  input_ids: FAIL - {e}")
            all_passed = False

        # loss_mask comparison
        try:
            np.testing.assert_array_equal(
                packed_A["loss_mask"],
                packed_B["loss_mask"],
            )
            loss_tokens = int(np.sum(packed_A["loss_mask"] > 0))
            print(f"  loss_mask: PASS ({loss_tokens} tokens with loss)")
        except AssertionError as e:
            print(f"  loss_mask: FAIL - {e}")
            all_passed = False

        # pixel_values comparison (use allclose for floating point)
        try:
            np.testing.assert_allclose(
                packed_A["pixel_values"],
                packed_B["pixel_values"],
                rtol=1e-5, atol=1e-5,
            )
            print(f"  pixel_values: PASS (shape={packed_A['pixel_values'].shape})")
        except AssertionError as e:
            print(f"  pixel_values: FAIL - {e}")
            all_passed = False

        # segment_ids comparison
        try:
            np.testing.assert_array_equal(
                packed_A["segment_ids"],
                packed_B["segment_ids"],
            )
            print(f"  segment_ids: PASS (unique={np.unique(packed_A['segment_ids'])})")
        except AssertionError as e:
            print(f"  segment_ids: FAIL - {e}")
            all_passed = False

        # position_ids comparison
        try:
            np.testing.assert_array_equal(
                packed_A["position_ids"],
                packed_B["position_ids"],
            )
            print(f"  position_ids: PASS")
        except AssertionError as e:
            print(f"  position_ids: FAIL - {e}")
            all_passed = False

        # image_segment_ids comparison
        try:
            np.testing.assert_array_equal(
                packed_A["image_segment_ids"],
                packed_B["image_segment_ids"],
            )
            print(f"  image_segment_ids: PASS ({packed_A['image_segment_ids']})")
        except AssertionError as e:
            print(f"  image_segment_ids: FAIL - {e}")
            all_passed = False

        # grid_mask comparison
        try:
            np.testing.assert_array_equal(
                packed_A["grid_mask"],
                packed_B["grid_mask"],
            )
            print(f"  grid_mask: PASS")
        except AssertionError as e:
            print(f"  grid_mask: FAIL - {e}")
            all_passed = False

        assert all_passed, "PreprocessedPackedVLMDataset does not match streaming+packing!"
        print("\n Packed data comparison test passed!")


@pytest.mark.entry
@skip_if_no_torch
def test_preprocessed_packed_loss_matches_hf():
    """
    Verify: per-segment loss from PRE-PACKED preprocessed data matches HF per-sample loss.

    This tests that PreprocessedPackedVLMDataset produces data that results in
    correct loss computation matching HuggingFace's model.

    Flow:
    1. Load raw data
    2. preprocess_and_pack_samples → PreprocessedPackedVLMDataset → packed sample
    3. Compute HF golden loss for each sample individually (no packing)
    4. Compute Levanter per-segment loss from packed sample
    5. Compare: each segment's loss should match corresponding HF sample's loss (0.02% threshold)
    """
    import torch
    import transformers.models.llava_onevision.modeling_llava_onevision as llava_modeling
    from transformers import AutoModelForVision2Seq, AutoTokenizer, AutoProcessor
    import haliax as hax
    import jax
    import jax.numpy as jnp
    import equinox as eqx
    from levanter.models.llava_onevision import LlavaOnevisionModel
    from levanter.trainer import TrainerConfig
    from levanter.data.vlm_packing import PreprocessedPackedVLMDataset
    from test_train_image_anyres import _load_levanter_config

    # Set JAX to use float32 matmul precision
    jax.config.update("jax_default_matmul_precision", "float32")

    grid_pinpoints = SINGLE_PATCH_GRID_PINPOINTS
    num_samples = 2  # Pack 2 samples together

    # Load tokenizer and processor
    qwen3_tokenizer = AutoTokenizer.from_pretrained(QWEN3_TOKENIZER, trust_remote_code=True)
    image_token_id = qwen3_tokenizer.convert_tokens_to_ids("<|image_pad|>")
    processor = AutoProcessor.from_pretrained(MODEL_NAME, trust_remote_code=True)

    config = PreprocessConfig(
        max_length=MAX_LENGTH,
        features_per_patch=FEATURES_PER_PATCH,
    )

    with tempfile.TemporaryDirectory() as tmpdir:
        # 1. Load raw data
        hf_dataset = get_real_data(num_samples=num_samples)
        raw_parquet = f"{tmpdir}/raw.parquet"
        hf_dataset.to_parquet(raw_parquet)

        # 2. Preprocess and pack, then load with PreprocessedPackedVLMDataset
        max_patches = num_samples  # disable_anyres: 1 image = 1 patch

        raw_samples = [hf_dataset[i] for i in range(num_samples)]
        packed_samples = preprocess_and_pack_samples(
            samples=raw_samples,
            processor=processor,
            tokenizer=qwen3_tokenizer,
            config=config,
            max_patches=max_patches,
            max_segments=num_samples,
        )

        packed_parquet = f"{tmpdir}/packed.parquet"
        save_packed_parquet(packed_samples, packed_parquet)

        packed_dataset = PreprocessedPackedVLMDataset(
            parquet_paths=[packed_parquet],
            image_processor=processor.image_processor,
            max_num_patches=max_patches,
            patch_size=384,
        )

        # Get the packed sample
        packed_batch = asyncio.get_event_loop().run_until_complete(
            packed_dataset.get_batch([0])
        )
        packed = packed_batch[0]

        # Verify packing worked
        assert packed['num_segments'] == num_samples, f"Expected {num_samples} segments, got {packed['num_segments']}"

        print(f"\n=== Packed Sample Info ===")
        print(f"  num_segments: {packed['num_segments']}")
        print(f"  segment_ids unique: {np.unique(packed['segment_ids'])}")
        print(f"  input_ids shape: {packed['input_ids'].shape}")
        print(f"  pixel_values shape: {packed['pixel_values'].shape}")
        print(f"  image_segment_ids: {packed['image_segment_ids']}")

        # 3. Compute HF golden losses (using prepare_test_data for HF-format pixel_values)
        test_pairs = prepare_test_data(
            parquet_path=raw_parquet,
            sample_indices=list(range(num_samples)),
            model_name=MODEL_NAME,
            max_length=MAX_LENGTH,
            max_num_patches=1,
            grid_pinpoints=grid_pinpoints,
            disable_anyres=True,
        )

        hf_model = AutoModelForVision2Seq.from_pretrained(
            MODEL_NAME,
            torch_dtype=torch.float32,
            trust_remote_code=True,
        )
        hf_model.model.config.image_grid_pinpoints = grid_pinpoints
        hf_model.model.config.vision_aspect_ratio = "single"
        hf_model.model.image_newline = None
        hf_model.config.image_token_index = image_token_id
        hf_model.eval()

        # Monkey-patch for disable_anyres
        original_image_size_to_num_patches = llava_modeling.image_size_to_num_patches
        llava_modeling.image_size_to_num_patches = lambda *args, **kwargs: 1

        hf_golden_losses = []
        try:
            for i, pair in enumerate(test_pairs):
                lev_seq_len = len(pair.lev.input_ids)
                hf_input_ids = torch.from_numpy(np.array(pair.lev.input_ids[:lev_seq_len])).unsqueeze(0)
                hf_pixel_values = torch.from_numpy(pair.hf.pixel_values).unsqueeze(0)
                hf_image_sizes = torch.from_numpy(pair.hf.image_sizes).unsqueeze(0)

                if hf_pixel_values.dim() == 5:
                    hf_pixel_values = hf_pixel_values[:, 0:1, :, :, :]

                hf_labels = hf_input_ids.clone().long()
                seq_len = hf_input_ids.shape[1]
                loss_mask_np = np.array(pair.lev.loss_mask)[:seq_len]
                mask_tensor = torch.from_numpy(loss_mask_np).unsqueeze(0)
                hf_labels[mask_tensor == 0] = -100

                with torch.no_grad():
                    hf_output = hf_model(
                        input_ids=hf_input_ids,
                        pixel_values=hf_pixel_values,
                        image_sizes=hf_image_sizes,
                        labels=hf_labels,
                    )
                    hf_golden_losses.append(hf_output.loss.item())
        finally:
            llava_modeling.image_size_to_num_patches = original_image_size_to_num_patches

        print(f"\n=== HF Golden Losses ===")
        for i, loss in enumerate(hf_golden_losses):
            print(f"  Sample {i}: {loss:.6f}")

        # 4. Compute Levanter per-segment loss from PACKED sample
        lev_config = _load_levanter_config(
            MODEL_NAME,
            enable_flash_attention=False,
            gradient_checkpointing=False,
            vision_aspect_ratio="single",
            image_grid_pinpoints=grid_pinpoints,
        )
        lev_config = lev_config.with_token_ids(image_token_id=image_token_id)
        trainer_config = TrainerConfig()

        with trainer_config.use_device_mesh(), hax.axis_mapping(trainer_config.compute_axis_mapping):
            converter = lev_config.hf_checkpoint_converter(ref_checkpoint=MODEL_NAME)
            lev_model = converter.load_pretrained(
                LlavaOnevisionModel,
                ref=MODEL_NAME,
                config=lev_config,
                axis_mapping=trainer_config.parameter_axis_mapping,
                dtype=jnp.float32,
                resize_vocab_to_match_tokenizer=False,
            )

            # Create JAX tensors from packed sample
            Batch = hax.Axis("batch", 1)
            Position = hax.Axis("position", len(packed['input_ids']))
            NumPatches = hax.Axis("num_patches", packed['pixel_values'].shape[0])
            Channels = hax.Axis("channels", 3)
            Height = hax.Axis("height", 384)
            Width = hax.Axis("width", 384)
            GridMask = hax.Axis("grid_mask", packed['pixel_values'].shape[0])

            input_ids = hax.named(jnp.array(packed['input_ids']).reshape(1, -1), (Batch, Position))
            pixel_values = hax.named(
                jnp.array(packed['pixel_values']).reshape(1, -1, 3, 384, 384),
                (Batch, NumPatches, Channels, Height, Width)
            )
            grid_mask = hax.named(
                jnp.array(packed['grid_mask']).reshape(1, -1),
                (Batch, GridMask)
            )
            loss_mask = hax.named(jnp.array(packed['loss_mask']).reshape(1, -1), (Batch, Position))
            segment_ids_jax = hax.named(jnp.array(packed['segment_ids']).reshape(1, -1), (Batch, Position))
            position_ids = hax.named(jnp.array(packed['position_ids']).reshape(1, -1), (Batch, Position))
            image_segment_ids_jax = hax.named(jnp.array(packed['image_segment_ids']).reshape(1, -1), (Batch, GridMask))

            # Create combined_mask for packing attention
            # Each position can only attend to positions in the same segment
            segment_ids_np = packed['segment_ids']
            combined_mask = np.zeros((len(segment_ids_np),), dtype=np.float32)
            for i, seg_id in enumerate(segment_ids_np):
                if seg_id >= 0:
                    combined_mask[i] = 1.0
            combined_mask_jax = hax.named(jnp.array(combined_mask).reshape(1, -1), (Batch, Position))

            from levanter.data.image import ImageTextExample as ImgTextEx

            batch_example = ImgTextEx(
                pixel_values=pixel_values,
                input_ids=input_ids,
                loss_mask=loss_mask,
                grid_mask=grid_mask,
                unpad_indices=None,
                combined_mask=combined_mask_jax,
                position_ids=position_ids,
            )

            # Compute per-token loss with packing support
            @eqx.filter_jit
            def compute_per_token_loss(model, example, segment_ids, image_segment_ids):
                from levanter.models.loss import fused_cross_entropy_loss_and_logsumexp_penalty

                grid_mask = getattr(example, "grid_mask", None)
                position_ids = getattr(example, "position_ids", None)
                combined_mask = getattr(example, "combined_mask", None)

                # Packing mode: pass segment_ids for attention masking and image_segment_ids for image indexing
                activations, lm_head = model.forward_with_activations(
                    example.input_ids,
                    pixel_values=example.pixel_values,
                    grid_mask=grid_mask,
                    unpad_indices=None,
                    combined_mask=combined_mask,
                    position_ids=position_ids,
                    segment_ids=segment_ids,
                    image_segment_ids=image_segment_ids,
                    key=None,
                )

                Pos = example.input_ids.resolve_axis("position")
                Embed = model.config.TextEmbed
                Vocab = model.Vocab

                targets = hax.roll(example.input_ids, -1, Pos)

                per_token_loss = fused_cross_entropy_loss_and_logsumexp_penalty(
                    pred_embeddings=activations,
                    pred_lm_head=lm_head,
                    Contract=Embed,
                    Label=Vocab,
                    target_y=targets,
                    reduction=None,
                    weight=None,
                    logsumexp_weight=0.0,
                    block_size=4096,
                )

                return per_token_loss

            per_token_loss = compute_per_token_loss(lev_model, batch_example, segment_ids_jax, image_segment_ids_jax)
            per_token_loss_np = np.array(per_token_loss.array)[0]  # (seq_len,)

            # Compute per-segment loss
            # HF convention: labels[i+1] controls whether loss at position i is included
            segment_ids_np = packed['segment_ids']
            loss_mask_np = packed['loss_mask']
            shifted_loss_mask = np.roll(loss_mask_np, -1)
            shifted_loss_mask[-1] = 0  # Last position has no valid target

            lev_segment_losses = []
            for seg_id in range(num_samples):
                # Include position i if: in this segment AND loss_mask[i+1] > 0
                seg_mask = (segment_ids_np == seg_id) & (shifted_loss_mask > 0)
                if seg_mask.sum() > 0:
                    seg_loss = per_token_loss_np[seg_mask].mean()
                    lev_segment_losses.append(float(seg_loss))
                else:
                    lev_segment_losses.append(0.0)

            print(f"\n=== Levanter Per-Segment Losses (from PreprocessedPackedVLMDataset) ===")
            for i, loss in enumerate(lev_segment_losses):
                print(f"  Segment {i}: {loss:.6f}")

            # 5. Compare losses
            print(f"\n=== Loss Comparison ===")
            all_passed = True
            for i in range(num_samples):
                hf_loss = hf_golden_losses[i]
                lev_loss = lev_segment_losses[i]
                diff = abs(hf_loss - lev_loss)
                rel_diff = diff / hf_loss if hf_loss != 0 else diff

                # Strict threshold: 0.02%
                status = "PASS" if rel_diff < 0.0002 else "FAIL"
                print(f"  Sample {i}: HF={hf_loss:.6f}, Lev={lev_loss:.6f}, rel_diff={rel_diff*100:.4f}% [{status}]")

                if rel_diff >= 0.0002:
                    all_passed = False

            assert all_passed, "Per-segment losses do not match HF golden losses!"
            print("\n PreprocessedPackedVLMDataset loss consistency test passed!")


