# Rendered outcome line for the trajectory header. ``verifier_output`` is the
# preferred signal because it's a clean PASS/FAIL string; ``result`` doubles as
# both an outcome and an error-state field, so we treat any non-numeric value
# there as a failure with the error name preserved.
_PASS_TAG = "This trajectory solved the task successfully."
_FAIL_TAG = "This trajectory failed to solve the task."


def _outcome_tag(verifier_output: str | None, result: str | None) -> str | None:
    if verifier_output:
        upper = verifier_output.upper()
        if "PASS" in upper:
            return _PASS_TAG
        if "FAIL" in upper:
            return _FAIL_TAG
    if result is None:
        return None
    stripped = result.strip()
    if not stripped:
        return None
    try:
        return _PASS_TAG if float(stripped) >= 1.0 else _FAIL_TAG
    except ValueError:
        # Error state (e.g. ``AgentTimeoutError``) — treat as failure but keep
        # the error name so downstream consumers can see what went wrong.
        return f"This trajectory failed to solve the task ({stripped})."


def _row_to_doc(source_slug: str):
    """Build a row→doc transform bound to a single cohort.

    The ``source`` field stamped on each doc is the full HF repo id so a
    downstream consumer that joins across cohorts can still tell them apart.
    """
    hf_repo = f"penfever/{source_slug}-minimax-m27-131k-traces"
    counter_kept = f"penfever_minimax_m27_traces/{source_slug}/kept"
    counter_dropped = f"penfever_minimax_m27_traces/{source_slug}/dropped"

    def row_to_doc(row: dict) -> list[dict]:
        conversations = row.get("conversations")
        if not conversations:
            counters.increment(counter_dropped)
            return []

        tag = _outcome_tag(row.get("verifier_output"), row.get("result"))
        rendered = "\n\n".join(render_role_message(m) for m in conversations)
        text = f"{tag}\n\n{rendered}" if tag else rendered

        counters.increment(counter_kept)
        return [
            {
                "id": hashlib.sha256(text.encode("utf-8")).hexdigest(),
                "text": text,
                "source": hf_repo,
            }
        ]

    return row_to_doc


def _transform(source_slug: str, input_path: str, output_path: str) -> None:
    pipeline = (
        Dataset.from_files(f"{input_path}/**/*.parquet")
        .flat_map(load_parquet_batched)
        .flat_map(_row_to_doc(source_slug))
        .write_parquet(f"{output_path}/data-{{shard:05d}}-of-{{total:05d}}.parquet", skip_existing=True)
    )
    ctx = ZephyrContext(
        name=f"penfever-{TEACHER}-{source_slug}-transform",
        resources=ResourceConfig(cpu=1, ram="32g"),
    )
    ctx.execute(pipeline)


def _cohort_steps(source_slug: str, hf_dataset_id: str, revision: str) -> tuple[StepSpec, StepSpec]:
    """Return ``(processed, normalize)`` for one cohort.

    The HF download is wired in as a ``deps`` entry on ``processed`` rather
    than returned in the chain — matches the ``gpt_oss_rollouts`` /
    ``swe_rebench_openhands`` convention where the download step is implicit
    in the deps DAG and only the user-visible (transform, normalize) pair
    appears in the chain tuple.
    """
    dl = download_hf_step(
        f"raw/penfever-traces/{TEACHER}/{source_slug}",
        hf_dataset_id=hf_dataset_id,
        revision=revision,
    )
    processed = StepSpec(
        name=f"processed/penfever-traces/{TEACHER}/{source_slug}",
        deps=[dl],
        fn=lambda output_path, _slug=source_slug, _dl=dl: _transform(
            source_slug=_slug,
            input_path=_dl.output_path,
            output_path=output_path,
        ),
        hash_attrs={"version": "v1", "teacher": TEACHER, "source_slug": source_slug},
    )
    normalized = normalize_step(
        name=f"normalized/penfever-traces/{TEACHER}/{source_slug}",
        download=processed,
    )
    return (processed, normalized)


def penfever_minimax_m27_traces_normalize_steps() -> dict[str, tuple[StepSpec, ...]]:
    """Return ``(processed, normalize)`` chains for every MiniMax-M2.7 cohort.

    Keyed by marin_name (``penfever-traces/minimax-m27-131k/<source_slug>``)
    so :func:`marin.datakit.sources._rows_flat` can project the family into
    per-cohort source rows.
    """
    return {
        f"penfever-traces/{TEACHER}/{slug}": _cohort_steps(slug, hf_id, revision)
        for slug, hf_id, revision in _COHORT_ENTRIES
    }
